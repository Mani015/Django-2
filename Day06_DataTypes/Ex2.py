x = list("Hello World")
print(x)
# ['H', 'e', 'l', 'l', 'o', ' ', 'W', 'o', 'r', 'l', 'd']

y = set("Hello World")
print(y)
# {'r', 'd', 'l', 'o', 'e', 'W', 'H', ' '}

z = tuple("Hello World")
print(z)
# ('H', 'e', 'l', 'l', 'o', ' ', 'W', 'o', 'r', 'l', 'd')

s = str("Hello World")
print(s)
# Hello World